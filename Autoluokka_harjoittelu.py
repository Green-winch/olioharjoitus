# Määrittele luokka "Auto" eri ominaisuuksilla, jotka kuvaavat auton ominaisuuksia
class Auto:
    merkki = ""
    vari = ""
    kilometrit = 0
    paino = 0
    vuosi = 0
    hinta = 0

# Luo tyhjä lista, johon tallennetaan auto-oliot
autot = []

# Luo uusi "Auto" luokan ilmentymä ja täytä sen ominaisuudet
uusiAuto = Auto()
uusiAuto.merkki = "Mersu"
uusiAuto.vari = "punainen"
uusiAuto.kilometrit = 68
uusiAuto.paino = 1860
uusiAuto.vuosi = 1994
uusiAuto.hinta = 200000

# Lisää "uusiAuto" ilmentymä "autot" listaan
autot.append(uusiAuto)

# Toista sama prosessi kahdelle muulle autolle
vanhaAuto = Auto()
vanhaAuto.merkki = "Volkswagen"
vanhaAuto.vari = "sininen"
vanhaAuto.kilometrit = 1650
vanhaAuto.paino = 1860
vanhaAuto.vuosi = 1948
vanhaAuto.hinta = 350000
autot.append(vanhaAuto)

luksuuriAuto = Auto()
luksuuriAuto.merkki = "Ferrari"
luksuuriAuto.vari = "punainen"
luksuuriAuto.kilometrit = 45
luksuuriAuto.paino = 996
luksuuriAuto.vuosi = 2020
luksuuriAuto.hinta = 999335
autot.append(luksuuriAuto)
